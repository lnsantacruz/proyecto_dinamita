CREATE OR REPLACE PROCEDURE VENTAS IS

    V_TOTAL NUMBER := 0;
    V_VENTAS VARCHAR2(1024);
    
    CURSOR C_TABLA_VENTAS IS
    SELECT ID_VENDEDOR, NOMBRE, PRODUCTO, PRECIO, COMISION, FECHA_VENTA
    FROM
        VENTAS_1;
BEGIN



DBMS_OUTPUT.PUT_LINE('INFORMACIÓN DE VENTAS');
DBMS_OUTPUT.PUT_LINE('------------------------');
DBMS_OUTPUT.PUT_LINE('------------------------');
DBMS_OUTPUT.PUT_LINE('------------------------');
DBMS_OUTPUT.PUT_LINE('ID_VENDEDOR | NOMBRE | PRODUCTO | PRECIO | COMISION | FECHA_VENTA');

FOR REC IN C_TABLA_VENTAS LOOP

    V_VENTAS := REC.ID_VENDEDOR || ' | ' || REC.NOMBRE || ' | ' || REC.PRODUCTO || ' | ' || REC.PRECIO || ' | ' || REC.COMISION || ' | ' || REC.FECHA_VENTA;
    DBMS_OUTPUT.PUT_LINE(V_VENTAS);
    V_TOTAL := V_TOTAL + 1;
    
    INSERT INTO VENTAS_2 VALUES (REC.ID_VENDEDOR, REC.NOMBRE, REC.PRODUCTO, REC.PRECIO, REC.COMISION, REC.FECHA_VENTA);
    
    
    
END LOOP;


    DBMS_OUTPUT.PUT_LINE('Total Registros De Ventas: ' || V_TOTAL);


END VENTAS;
SET SERVEROUTPUT ON;
EXECUTE VENTAS;

